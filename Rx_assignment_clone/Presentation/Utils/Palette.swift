import UIKit

enum Palette {
    static var watermelon: UIColor {
        return UIColor(red: 1.0, green: 81.0 / 255.0, blue: 96.0 / 255.0, alpha: 1.0)
    }
    
    static var brownishGray: UIColor {
        return UIColor(white: 119.0 / 255.0, alpha: 1.0)
    }
    
    static var veryLightPink: UIColor {
        return UIColor(white: 238.0 / 255.0, alpha: 1.0)
    }
    
    static var black: UIColor {
        return UIColor(white: 31.0 / 255.0, alpha: 1.0)
    }
    
    static var black20: UIColor {
        return UIColor(white: 0.0, alpha: 0.2)
    }
    
    static var white: UIColor {
        return UIColor(white: 1.0, alpha: 1.0)
    }
}
